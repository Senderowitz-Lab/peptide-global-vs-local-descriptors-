    "def feature_reduction(train_set, test_set, var_threshold=0.05, corr_threshold=0.8):\n",
    "    print('Number of features originally: ', len(train_set.columns))\n",
    "    print(\"Removing Constant and Nearly Constant Features\")\n",
    "    var_threshold = VarianceThreshold(threshold=var_threshold)\n",
    "    var_threshold.fit(train_set)\n",
    "\n",
    "    indices_features_retained = var_threshold.get_support(indices=True)\n",
    "    train_set_reduced1 = train_set.iloc[:, indices_features_retained]\n",
    "\n",
    "    print('Features retained after elimination of constant and nearly constant features:', len(train_set_reduced1.columns))\n",
    "\n",
    "    print(\"Removing Correlated Features\")\n",
    "\n",
    "    # Calculate the correlation matrix\n",
    "    corr_matrix = train_set_reduced1.corr()\n",
    "    iters = range(len(corr_matrix.columns) - 1)\n",
    "    drop_cols = []\n",
    "\n",
    "    # Iterate through the correlation matrix and compare correlations\n",
    "    for i in iters:\n",
    "        for j in range(i+1):\n",
    "            item = corr_matrix.iloc[j:(j+1), (i+1):(i+2)]\n",
    "            col = item.columns\n",
    "            row = item.index\n",
    "            val = abs(item.values)\n",
    "\n",
    "            # If correlation exceeds the threshold\n",
    "            if val >= corr_threshold:\n",
    "                # Print the correlated features and the correlation value\n",
    "                print(col.values[0], \"|\", row.values[0], \"|\", round(val[0][0], 2))\n",
    "                drop_cols.append(col.values[0])\n",
    "\n",
    "    # Drop one of each pair of correlated columns\n",
    "    drops = set(drop_cols)\n",
    "    train_set_reduced2 = train_set_reduced1.drop(columns=drops)\n",
    "    print('Removed Columns {}'.format(drops))\n",
    "    print('Number of features retained: ', len(train_set_reduced2.columns))\n",
    "\n",
    "    test_set_reduced = test_set[train_set_reduced2.columns]\n",
    "\n",
    "    return train_set_reduced2, test_set_reduced\n",
